//Dado o tamanho do lado de um quadrado, calcular a área e o perímetro do mesmo.

const ladoQuadrado = Number(prompt("Qual é o lado do quadrado ?"))

areaQuadrado = ladoQuadrado * ladoQuadrado
perimetroQuadrado = ladoQuadrado * 4

alert("A área do quadrado é " + areaQuadrado)
alert("O perímetro do quadrado é " + perimetroQuadrado)